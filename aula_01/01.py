soma = n1 + n2
sub = n1 - n2
mult = n1 * n2
div = n1 / n2

print(f"A soma entre os números é {soma}")
print(f"A subtração entre os números é {sub}")
print(f"A multiplicação entre os números é {mult}")
print(f"A divisão entre os números é {div}")